def get_reqmat_choices() -> list[tuple[str, int]]:
    names = []
    for name in dir(idaapi):
        if not name.startswith("MMAT_"):
            continue
        value = getattr(idaapi, name, None)
        if isinstance(value, int):
            names.append((name, value))

    if not names:
        return [("MMAT_LOCOPT", 0)]

    return sorted(names, key=lambda item: (item[1], item[0]))


def default_reqmat() -> int:
    return getattr(idaapi, "MMAT_LOCOPT", get_reqmat_choices()[0][1])


def get_cmat_choices() -> list[tuple[str, int]]:
    names = []
    for name in dir(idaapi):
        if not name.startswith("CMAT_"):
            continue
        value = getattr(idaapi, name, None)
        if isinstance(value, int):
            names.append((name, value))

    if not names:
        return [("CMAT_FINAL", 0)]

    return sorted(names, key=lambda item: (item[1], item[0]))


def default_cmat() -> int:
    return getattr(idaapi, "CMAT_FINAL", get_cmat_choices()[0][1])


