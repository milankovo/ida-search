"""
IDA Form UI for the advanced search dialog.

Provides ``binary_search_form`` -- a modal ``idaapi.Form`` with:

- Pattern text input with live preview of generated byte patterns.
- Type specifier dropdown (``parse.Types``).
- Endian radio buttons (Little / Big / Both).
- Backend checkboxes (byte search, instruction operands, microcode,
  ctree, pseudocode text).  Hex-Rays backends are hidden when the
  decompiler is not available.
- Backend applicability notes in the preview area.

The form is opened by ``advanced_search_ah_t`` in ``plugin.py`` via
the ``Alt-Shift-B`` hotkey.  After ``Execute()`` returns 1 (OK), the
caller extracts the pattern string, endian, and backend bitmask
using ``get_pattern()``, ``get_endian()``, and ``get_backends_mask()``.

Backend bitmask bit indices are exported as module-level constants:
``BACKEND_BYTES``, ``BACKEND_INSN``, ``BACKEND_MICRO``,
``BACKEND_CTREE``, ``BACKEND_PSEUDO``.
Case sensitivity is controlled by a separate Options group box.
"""

